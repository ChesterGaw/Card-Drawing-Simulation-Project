
public class Card {
	private int suit;
	private int count;
	
	public Card(int suit, int count) {
		suit = this.suit;
		count = this.count;
	}

	public int getSuit() {
		return suit;
	}

	public void setSuit(int suit) {
		this.suit = suit;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	
	
}
